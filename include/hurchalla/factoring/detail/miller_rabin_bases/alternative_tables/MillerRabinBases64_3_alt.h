// This alternative version uses a larger table than the normal version, and so
// it's generally not preferred.
// However, it reads only one cache line per table access (the normal version
// reads two lines per access), and it uses a smaller and faster hash function.

// This 32KB hash table lets you determine the primality of any 64 bit unsigned
// int number, via miller-rabin primality testing using 3 bases.
// This test permits numbers that are even, as well as of course odd numbers.
// Note however that montgomery arithmetic (which is one way to implement the
// miller-rabin test) always requires odds.

// see MillerRabinBases.h for why this template uses a DUMMY parameter.
